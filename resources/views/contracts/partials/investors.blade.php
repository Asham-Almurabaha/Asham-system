  }

  function getNewRows() { return qsa("tr.new-row", tableBody); }

  function getNewPercentage() {
    return getNewRows().reduce((sum, tr) => {
      const pct = parseInt(tr.querySelector('[name*="share_percentage"]')?.value || "0", 10) || 0;

    // ุงููููุฉ: ุฃุซูุงุก ุงููุชุงุจุฉ ูุญูููุง ููุณุจุฉ ุตุญูุญุฉ ุจุฏูู ูุตู ูููุชุจูู
    valInput.addEventListener("input", () => {
